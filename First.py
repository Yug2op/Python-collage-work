num1 = 10
num2 = 5
#arithmetic operators
addition = num1 + num2
print(f"Addition: {num1} + {num2} = {addition}")

subtraction = num1 - num2
print(f"Subtraction: {num1} - {num2} = {subtraction}")

multiplication = num1 * num2
print(f"Multiplication: {num1} * {num2} = {multiplication}")

division = num1 / num2
print(f"Division: {num1} / {num2} = {division}")

modulus = num1 % num2
print(f"Modulus: {num1} % {num2} = {modulus}")

exponentiation = num1 ** num2
print(f"Exponentiation: {num1} ** {num2} = {exponentiation}")

floor_division = num1 // num2
print(f"Floor Division: {num1} // {num2} = {floor_division}")


#assignment operators
num1 += num2
print(f"Addition Assignment: num1 += num2 -> {num1}")

num1 -= num2
print(f"Subtraction Assignment: num1 -= num2 -> {num1}")

num1 *= num2
print(f"Multiplication Assignment: num1 *= num2 -> {num1}")

num1 /= num2
print(f"Division Assignment: num1 /= num2 -> {num1}")

num1 %= num2
print(f"Modulus Assignment: num1 %= num2 -> {num1}")

num1 **= num2
print(f"Exponentiation Assignment: num1 **= num2 -> {num1}")

num1 //= num2
print(f"Floor Division Assignment: num1 //= num2 -> {num1}")

#comparison operators
print(num1 > num2)
print(num1 < num2)
print(num1 == num2)
print(num1 != num2)
print(num1 >= num2)
print(num1 <= num2)
